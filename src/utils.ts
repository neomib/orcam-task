

export function letterToColor(letter: string) {
    switch (letter.toUpperCase()) {
        case "A":
            return "C0CFDD";
        case "B":
            return "fccc8a";
        case "C":
            return "fcd4e3";
        case "D":
            return "95eff1";
        case "E":
            return "C4B4CF";
        case "F":
            return "ffccbb";
        case "G":
            return "4EA3DE";
        case "H":
            return "E64A65";
        case "I":
            return "D4B5B0";
        case "J":
            return "00909E";
        case "K":
            return "A2A8D3";
        case "L":
            return "BBBBBB";
        case "M":
            return "74BBCA";
        case "N":
            return "98e0ff";
        case "O":
            return "AAC9A7";
        case "P":
            return "8FC1D4";
        case "Q":
            return "efcfff";
        case "R":
            return "CD5D7D";
        case "S":
            return "94ABD9";
        case "T":
            return "6886C5";
        case "U":
            return "49AA91";
        case "V":
            return "6F69AC";
        case "W":
            return "127681";
        case "X":
            return "43A5FF";
        case "Y":
            return "857671";
        case "Z":
            return "9BA4B4";
        default:
            return "C0CFDD";
    }
}